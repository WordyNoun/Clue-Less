public class Weapon {
    public String weaponName;
    
    public Weapon(String name) {
        this.weaponName = name;
    }
    
    public String getWeaponName() {
        return this.weaponName;
    }
    
    public String toString() {
        String output;
        
        output = this.weaponName;
        
        return output;
    }
    
}
